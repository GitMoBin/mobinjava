import java.util.*;
class StringIndexOfMethod
{
	public static void main(String[] args) 
	{
		String str = "bMobin";
		int a = str.indexOf('b');
		int b = str.lastIndexOf('b');
		System.out.println("First Index of : "+a);//First index of character
		System.out.println("Last Index of : "+b);//Last index of character
	}
}
